-- Joining departments and dept_manager tables
SELECT d.dept_name, -- this will select the columns you need
     dm.emp_no,
     dm.from_date,
     dm.to_date
FROM d -- this will select the table from which the data is to be fetched
INNER JOIN dm --  this will help join the first table selected using "FROM" with the second table mentioned after "INNER JOIN"
ON d.dept_no = dm.dept_no; -- This will specify the common column on which the join is performed

-- Joining retirement_info and dept_emp tables
SELECT retirement_info.emp_no, -- This will select the table name and its corresponding column, table 1 and table 2
    retirement_info.first_name,
	retirement_info.last_name,
    dept_emp.to_date
FROM retirement_info
LEFT JOIN dept_emp
ON retirement_info.emp_no = dept_emp.emp_no;

SELECT ri.emp_no, -- Same query as above but using aliases
    ri.first_name,
	ri.last_name,
    de.to_date
FROM retirement_info as ri
LEFT JOIN dept_emp as de
ON ri.emp_no = de.emp_no;

SELECT ri.emp_no,
    ri.first_name,
